    const selectedLevel = parseInt(event.target.value, 10);
    
    // æ›´æ–°éŠæˆ²æ ¸å¿ƒçš„é—œå¡ç´¢å¼•è®Šæ•¸
    currentLevelIndex = selectedLevel;
    
