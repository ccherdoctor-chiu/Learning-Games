document.addEventListener('DOMContentLoaded', () => {
  // --- 元素獲取 ---
  const mazeContainer = document.getElementById('maze');
  const messageElem = document.getElementById('message');
  const stepsElem = document.getElementById('steps');
  const resetButton = document.getElementById('reset-button');
  const levelElem = document.getElementById('current-level'); // 新增：獲取關卡顯示元素

 // 新增：取得下拉選單元素
  const levelSelect = document.getElementById('level-select');

  // 新增：動態生成選關選項
  for (let i = 0; i < LEVELS.length; i++) {
    const option = document.createElement('option');
    option.value = i;
    option.textContent = i + 1; // 顯示從 1 開始的關卡數
    levelSelect.appendChild(option);
  }
// 監聽下拉選單的變更事件
  levelSelect.addEventListener('change', (event) => {
    // 從事件中取得玩家選擇的關卡索引值（此時為字串），並轉換為數字
    const selectedLevel = parseInt(event.target.value, 10);
    
    // 更新遊戲核心的關卡索引變數
    currentLevelIndex = selectedLevel;
    
    // 呼叫您既有的函式，載入對應的關卡
    loadLevel(currentLevelIndex);
    });
  // --- 遊戲狀態變數 ---
  let cellElements = [];
  let playerPos = null;
  let startPosition = null;
  let finishPosition = null;
  let steps = 0;
  let gameFinished = false;
  let currentLevelIndex = 0; // 新增：目前關卡索引，從 0 開始

  // --- 重構：載入指定關卡的函式 ---
  function loadLevel(levelIndex) {
    const MAZE_LAYOUT = LEVELS[levelIndex];
    const rows = MAZE_LAYOUT.length;
    const cols = MAZE_LAYOUT[0].length;
    
    // 清空舊的迷宮
    mazeContainer.innerHTML = '';
    cellElements = [];

    // 動態設置 CSS Grid 的欄數和列數
    mazeContainer.style.gridTemplateColumns = `repeat(${cols}, 40px)`;
    mazeContainer.style.gridTemplateRows = `repeat(${rows}, 40px)`;

    // 重新建立格子
    for (let r = 0; r < rows; r++) {
      cellElements[r] = [];
      for (let c = 0; c < cols; c++) {
        const cell = document.createElement('div');
        cell.classList.add('cell');
        cell.dataset.row = r;
        cell.dataset.col = c;
        const value = MAZE_LAYOUT[r][c];
        if (value === 1) {
          cell.classList.add('wall');
        } else {
          cell.classList.add('path');
          if (value === 2) {
            cell.classList.add('start');
            startPosition = { row: r, col: c };
          } else if (value === 3) {
            cell.classList.add('finish');
            finishPosition = { row: r, col: c };
          }
        }
        mazeContainer.appendChild(cell);
        cellElements[r][c] = cell;
      }
    }
    
    // 重置狀態
    playerPos = { ...startPosition };
    steps = 0;
    stepsElem.textContent = steps;
    levelElem.textContent = levelIndex + 1; // 更新關卡顯示
    levelSelect.value = levelIndex; // 同步更新下拉選單的顯示
    gameFinished = false;
    messageElem.textContent = '請使用方向鍵開始遊戲';
    
    placePlayer(playerPos);
    // 確保鍵盤監聽是啟用的
    document.removeEventListener('keydown', handleKeydown);
    document.addEventListener('keydown', handleKeydown);
  }
  
  // 放置玩家圖示
  function placePlayer(pos) {
    document.querySelectorAll('.cell.player').forEach((el) => {
      el.classList.remove('player');
      el.textContent = '';
    });
    const cell = cellElements[pos.row][pos.col];
    cell.classList.add('player');
    cell.textContent = '🐱';
  }
  
  // 處理移動邏輯
  function tryMove(newRow, newCol) {
    if (gameFinished) return;
    
    const MAZE_LAYOUT = LEVELS[currentLevelIndex];
    const rows = MAZE_LAYOUT.length;
    const cols = MAZE_LAYOUT[0].length;
    
    if (newRow < 0 || newRow >= rows || newCol < 0 || newCol >= cols) {
      return;
    }
    
    const cellValue = MAZE_LAYOUT[newRow][newCol];
    if (cellValue === 1) {
      return;
    }
    
    const previousCell = cellElements[playerPos.row][playerPos.col];
    if (!previousCell.classList.contains('start')) {
      previousCell.classList.add('visited');
    }
    
    playerPos = { row: newRow, col: newCol };
    steps += 1;
    stepsElem.textContent = steps;
    placePlayer(playerPos);
    
    // --- 修改：通關邏輯 ---
    if (newRow === finishPosition.row && newCol === finishPosition.col) {
      const cell = cellElements[newRow][newCol];
      cell.classList.add('finish-animation');
      document.removeEventListener('keydown', handleKeydown); // 暫停輸入
      
      // 檢查是否為最後一關
      if (currentLevelIndex < LEVELS.length - 1) {
        // 進入下一關
        messageElem.textContent = `太棒了！第 ${currentLevelIndex + 1} 關完成！`;
        setTimeout(() => {
          currentLevelIndex++;
          loadLevel(currentLevelIndex);
        }, 2000); // 延遲 2 秒後載入下一關
      } else {
        // 全部通關
        gameFinished = true;
        messageElem.textContent = '恭喜！你已完成所有挑戰！';
      }
    }
  }

  // 處理鍵盤事件 (內容不變)
  function handleKeydown(event) {
    switch (event.key) {
      case 'ArrowUp': case 'w': case 'W':
        tryMove(playerPos.row - 1, playerPos.col);
        break;
      case 'ArrowDown': case 's': case 'S':
        tryMove(playerPos.row + 1, playerPos.col);
        break;
      case 'ArrowLeft': case 'a': case 'A':
        tryMove(playerPos.row, playerPos.col - 1);
        break;
      case 'ArrowRight': case 'd': case 'D':
        tryMove(playerPos.row, playerPos.col + 1);
        break;
      default:
        return;
    }
    event.preventDefault();
  }

  // --- 修改：重置遊戲函式 ---
  // 現在的重置是重新載入「目前」的關卡
  function resetGame() {
    // 如果遊戲已完全結束，點擊重置將從第一關開始
    if (gameFinished && currentLevelIndex === LEVELS.length - 1) {
        currentLevelIndex = 0;
    }
    loadLevel(currentLevelIndex);
  }

  resetButton.addEventListener('click', resetGame);

  // --- 初始啟動 ---
  loadLevel(currentLevelIndex); // 載入第一關

});